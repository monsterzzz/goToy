package easy

/*
给定一个整数 n，返回 n! 结果尾数中零的数量。

*/

func trailingZeroes(n int) int {

	res := 0

	/*
		5! = 1 * 2 * 3 * 4 *5 = 120 = 12 * 10 = 12 * 2 * 5
		2 总是比 5 多  1 * 2 * 3 * 4 * 5 = 1 * 2 *3 *2 *2 *5
		所以计算5的个数就行

		又因为
		25 = 5 * 5
		25! = 1*2*3*4*5 * ... * 25 = 1 * 2 *3 *4 *5 *... * 5 * 5
		需要多了一个5
		所以结果为 n/5 + n/5/5 + n / 5 / 5/5  + ...

	*/

	for n >= 5 {
		res += n / 5
		n = n / 5
	}
	return res
}
